// Funciones auxiliares mejoradas
export const getCategoria = (id) => categorias.find(cat => cat.id === id);

export const getProductosPorCategoria = (categoriaId) => {
  const categoria = getCategoria(categoriaId);
  return categoria ? categoria.productos : [];
};

export const getAllCategorias = () => categorias.map(({ id, nombre, descripcion, productos }) => ({
  id,
  nombre,
  descripcion,
  count: productos.length
}));

export const buscarProductos = (termino) => {
  const resultados = [];
  categorias.forEach(categoria => {
    categoria.productos.forEach(producto => {
      if (producto.nombre.toLowerCase().includes(termino.toLowerCase()) || 
          producto.descripcion.toLowerCase().includes(termino.toLowerCase())) {
        resultados.push({
          ...producto,
          categoria: categoria.nombre
        });
      }
    });
  });
  return resultados;
};

export const getCategoriaPaths = () => categorias.map(categoria => ({
  id: categoria.id,
  nombre: categoria.nombre
}));
